const playlistLoadCache = new Map();


function getPlaylistBaseUrl(station) {
  if (!station?.playlistUrl) return "";
  return station.playlistUrl.replace(/\/[^/]*$/, "/");
}

function isAbsoluteUrl(value) {
  return /^https?:\/\//i.test(value || "");
}

function resolvePlaylistAsset(value, station) {
  if (!value || isAbsoluteUrl(value) || value.startsWith("/") || value.startsWith("data:")) {
    return value;
  }
  return `${getPlaylistBaseUrl(station)}${value}`;
}

function normalizeTrackAssetPaths(track, station) {
  if (!track) return track;
  return {
    ...track,
    cover: resolvePlaylistAsset(track.cover ?? track.image, station),
    image: resolvePlaylistAsset(track.image ?? track.cover, station),
    albumCover: resolvePlaylistAsset(track.albumCover, station)
  };
}

function applyRemotePlaylist(station, payload) {
  const remoteArtist = payload?.artist ?? station.featuredTrack.artist;
  const latestFromRepo = payload?.latestTrack;

  if ((payload?.featuredTrack || latestFromRepo) && Array.isArray(payload?.tracks)) {
    const remoteFeaturedTrack = {
      ...(payload?.featuredTrack ?? {}),
      ...(latestFromRepo
        ? {
            title: latestFromRepo.title,
            album: latestFromRepo.album,
            cover: latestFromRepo.image,
            audioSrc: latestFromRepo.audio
          }
        : {})
    };

    station.featuredTrack = normalizeTrackAssetPaths({
      ...station.featuredTrack,
      ...remoteFeaturedTrack,
      artist: remoteFeaturedTrack.artist ?? remoteArtist,
      duration: remoteFeaturedTrack.duration ?? null
    }, station);

    station.tracks = payload.tracks
      .filter((track) => track.title !== station.featuredTrack.title)
      .map((track) => normalizeTrackAssetPaths({
        artist: remoteArtist,
        duration: null,
        ...track,
        artist: track.artist ?? remoteArtist,
        duration: track.duration ?? null
      }, station));
    return;
  }

  if (Array.isArray(payload?.songs) && payload.songs.length > 0) {
    const [featuredTrack, ...tracks] = payload.songs;

    station.featuredTrack = normalizeTrackAssetPaths({
      ...station.featuredTrack,
      ...featuredTrack,
      artist: featuredTrack.artist ?? remoteArtist,
      duration: featuredTrack.duration ?? null
    }, station);

    station.tracks = tracks.map((track) => normalizeTrackAssetPaths({
      artist: remoteArtist,
      duration: null,
      ...track,
      artist: track.artist ?? remoteArtist,
      duration: track.duration ?? null
    }, station));
  }
}

export async function ensureStationPlaylistLoaded(stationId) {
  const station = getStationById(stationId);

  if (!station?.playlistUrl) {
    return station;
  }

  if (playlistLoadCache.has(stationId)) {
    return playlistLoadCache.get(stationId);
  }

  const request = fetch(`${station.playlistUrl}?cb=${Date.now()}`, { cache: "no-store" })
    .then((response) => {
      if (!response.ok) {
        throw new Error(`Failed to load playlist for ${stationId}`);
      }
      return response.json();
    })
    .then(async (payload) => {
      if (payload?.latestUrl) {
        try {
          const latestResponse = await fetch(`${payload.latestUrl}?cb=${Date.now()}`, { cache: "no-store" });
          if (latestResponse.ok) {
            payload.latestTrack = await latestResponse.json();
          }
        } catch {
          // Keep the fallback featured track if latest.json fails.
        }
      }
      applyRemotePlaylist(station, payload);
      return station;
    })
    .catch(() => station);

  playlistLoadCache.set(stationId, request);
  return request;
}
