export function getStationById(stationId) {
  return stations.find((station) => station.id === stationId) ?? stations[0];
}

export function getStationIndex(stationId) {
  return stations.findIndex((station) => station.id === stationId);
}

export function getStationTracks(station) {
  const featuredTrack = {
    ...station.featuredTrack,
    album: station.featuredTrack.album ?? "Single"
  };

  const additionalTracks = station.tracks.map((track) => ({
    artist: station.featuredTrack.artist,
    duration: null,
    ...track
  }));

  return [featuredTrack, ...additionalTracks];
}

